    private void escribir_TXT() {

        FileWriter fichero = null;
        PrintWriter pw;
        try {
            fichero = new FileWriter(jTextField1.getText());
            pw = new PrintWriter(fichero);

            pw.print(res.replace("\033[1m", "").replace("\033[0m", ""));

        } catch (Exception e) {
            System.err.println(e.getMessage());
        } finally {
            try {

                if (null != fichero) {
                    fichero.close();
                }
            } catch (Exception e2) {
                System.err.println(e2.getMessage());
            }
        }
    }
    

